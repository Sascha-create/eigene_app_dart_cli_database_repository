  sleep(Duration(milliseconds: ms));
  print('         *   * * "#. ##"       *');
  sleep(Duration(milliseconds: ms));
  print('           *       "###');
  sleep(Duration(milliseconds: ms));
  print('                     "##');
  sleep(Duration(milliseconds: ms));
  print("                      ##.");
  sleep(Duration(milliseconds: ms));
  print("                      .##:");
  sleep(Duration(milliseconds: ms));
  print("                      :###");
  sleep(Duration(milliseconds: ms));
  print("                      ;###");
  sleep(Duration(milliseconds: ms));
  print("                    ,####.");
  sleep(Duration(milliseconds: ms));
  print("        /\\/\\/\\/\\/\\/.######.\\/\\/\\/\\/\\");
